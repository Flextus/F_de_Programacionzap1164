#include "potenciatest.h"

namespace matespace
{
    double Arithmetic::sumayo(double a, double b)
    {
        return a + b;
    }

    double Arithmetic::restayo(double a, double b)
    {
        return a - b;
    }

    double Arithmetic::multiyo(double a, double b)
    {
        return a * b;
    }

    double Arithmetic::diviyo(double a, double b)
    {
        return a / b;
    }
}